@implementation UITextView (Lines)


- (CGSize)sizeNeeded:(NSRange)range text:(NSString*)text{
    
    
    // limit the number of lines in textview
    NSString* newText = [self.text stringByReplacingCharactersInRange:range withString:text];
    
    // pretend there's more vertical space to get that extra line to check on
    CGSize tallerSize = CGSizeMake(self.frame.size.width-15, self.frame.size.height*2);
    
    CGSize newSize = [newText sizeWithFont:self.font constrainedToSize:tallerSize lineBreakMode:UILineBreakModeWordWrap];
    
    return newSize;
}

@end
